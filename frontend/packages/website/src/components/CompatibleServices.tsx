type Tab = 'russia' | 'abroad';

/* Russian flag as inline SVG (emoji shows as "RU" on many systems) */
const RussiaFlag = () => (
  <svg viewBox="0 0 36 24" width="22" height="15" style={{ borderRadius: 2 }}>
    <rect width="36" height="8" fill="#fff"/>
    <rect y="8" width="36" height="8" fill="#0039A6"/>
    <rect y="16" width="36" height="8" fill="#D52B1E"/>
  </svg>
);

export default function CompatibleServices() {
  const [tab, setTab] = useState<Tab>('russia');
  const [hoverProgress, setHoverProgress] = useState(0);
  const containerRef = useRef<HTMLDivElement>(null);
  const [waveKey, setWaveKey] = useState(0);

  const handleMouseMove = useCallback((e: React.MouseEvent<HTMLDivElement>) => {
    if (!containerRef.current) return;
    const rect = containerRef.current.getBoundingClientRect();
    const x = (e.clientX - rect.left) / rect.width;
    const p = (x - 0.5) * 2;
    setHoverProgress(p);
    const newTab: Tab = p < -0.1 ? 'russia' : p > 0.1 ? 'abroad' : (tab as Tab);
    if (newTab !== tab) {
      setTab(newTab);
      setWaveKey((k) => k + 1);
    }
  }, [tab]);

  const handleMouseLeave = useCallback(() => {
    setHoverProgress(0);
  }, []);

  /* "Я в России" shows foreign services, "Я за границей" shows Russian services */
  const items = tab === 'russia' ? IN_RUSSIA : ABROAD;

  return (
    <section className="section" id="compatible">
      <div className="section-title-row">
        <h2>Свобода выбора</h2>
        <div className="section-title-line" />
      </div>

      <div
        className="compat-hover-zone"
        ref={containerRef}
        onMouseMove={handleMouseMove}
        onMouseLeave={handleMouseLeave}
      >
        <div className={`compat-label compat-label-left${tab === 'russia' ? ' active' : ''}`}>
          <span className="compat-label-flag"><RussiaFlag /></span>
          <span>Я в России</span>
        </div>
        <div className="compat-hover-divider">
          <div className="compat-hover-glow" style={{ transform: `translateX(${hoverProgress * 40}px)` }} />
        </div>
        <div className={`compat-label compat-label-right${tab === 'abroad' ? ' active' : ''}`}>
          <span className="compat-label-flag">🌍</span>
          <span>Я за границей</span>
        </div>
      </div>

      <div className="compat-grid" key={`${tab}-${waveKey}`}>
        {items.map((item, idx) => (
          <div
            className="compat-item compat-item-wave"
            key={item.name}
            style={{ animationDelay: `${idx * 0.06}s` }}
          >
            <div className="compat-icon" style={{ color: item.color }}>
              {SERVICE_ICONS[item.icon]}
            </div>
            <span className="compat-name">{item.name}</span>
          </div>
        ))}
      </div>
    </section>
  );
}
